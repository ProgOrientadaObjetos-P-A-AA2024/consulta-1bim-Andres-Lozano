# Crear una lista de números
numeros = [5, 3, 8, 1, 2]

# Ordenar la lista
numeros.sort()
print(numeros)  # Imprime: [1, 2, 3, 5, 8]

# Invertir la lista
numeros.reverse()
print(numeros)  # Imprime: [8, 5, 3, 2, 1]

# Obtener la longitud de la lista
longitud = len(numeros)
print(longitud)  # Imprime: 5

# Verificar si un elemento está en la lista
existe = 3 in numeros
print(existe)  # Imprime: True
